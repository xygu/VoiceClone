def _reg_fonts():
    global _FONT_OK, CN, CN_B
    if _FONT_OK:
        return
    # Use STHeiti SC (Simplified Chinese) — available on macOS
    font_path = "/System/Library/Fonts/STHeiti Medium.ttc"
    if os.path.exists(font_path):
        try:
            pdfmetrics.registerFont(TTFont("STHeitiSC", font_path, subfontIndex=1))
            from reportlab.pdfbase.pdfmetrics import registerFontFamily
            registerFontFamily("STHeitiSC", normal="STHeitiSC", bold="STHeitiSC",
                               italic="STHeitiSC", boldItalic="STHeitiSC")
            CN = "STHeitiSC"
            CN_B = "STHeitiSC"
        except Exception as e:
            print(f"Warning: CJK font registration failed: {e}. Using Helvetica fallback.")
            CN = "Helvetica"
            CN_B = "Helvetica-Bold"
    else:
        CN = "Helvetica"
        CN_B = "Helvetica-Bold"
    _FONT_OK = True

# ── Styles ────────────────────────────────────────────────────────────────────
def _styles():
    _reg_fonts()
    ss = getSampleStyleSheet()
    def _add(name, **kw):
        ss.add(ParagraphStyle(name, **kw))
    _add("T",  parent=ss["Title"],    fontName=CN_B, fontSize=18, leading=26, alignment=TA_CENTER, spaceAfter=6*mm)
    _add("ST", parent=ss["Normal"],   fontName=CN,   fontSize=11, leading=16, alignment=TA_CENTER, textColor=HexColor("#555"), spaceAfter=8*mm)
    _add("H1", parent=ss["Heading1"], fontName=CN_B, fontSize=15, leading=22, spaceBefore=8*mm, spaceAfter=4*mm, textColor=HexColor("#1a1a2e"))
    _add("H2", parent=ss["Heading2"], fontName=CN_B, fontSize=12.5, leading=18, spaceBefore=5*mm, spaceAfter=3*mm, textColor=HexColor("#16213e"))
    _add("B",  parent=ss["Normal"],   fontName=CN,   fontSize=10, leading=16, alignment=TA_JUSTIFY, spaceAfter=2*mm, firstLineIndent=20)
    _add("BN", parent=ss["Normal"],   fontName=CN,   fontSize=10, leading=16, alignment=TA_JUSTIFY, spaceAfter=2*mm)
